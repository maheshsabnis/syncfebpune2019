describe("mocking ajax calls", function() {
  describe("my ajax call suites", function() {
    // When you want to mock out all ajax calls
    // across an entire suite,
    // use install() in a beforeEach.
    beforeEach(function() {
      jasmine.Ajax.install();
    });
    // Because jasmine - ajax stubs out the global
    // XMLHttpRequest for the page,
    // you'll want to uninstall()
    // in an afterEach so specs or
    // setup that expect to make a real ajax
    // request can.

    afterEach(function() {
      jasmine.Ajax.uninstall();
    });
    // make the normal request
    // Make your requests as normal.Jasmine - Ajax
    // mocks out your request at the XMLHttpRequest
    // object, so should be compatible with
    // other libraries that do ajax requests.
    it("should make an ajax call", function() {
      var completedFunction = jasmine.createSpy("success");
      // call using XmlHttpRequest
      var xhr = new XMLHttpRequest();
      xhr.onreadystatechange = function(args) {
        if (this.readyState == this.DONE) {
          completedFunction(this.responseText);
        }
      };

      // start the call
      xhr.open(
        "GET",
        "http://apiapptrainingservice.azurewebsites.net/api/Products"
      );
      xhr.send();
      // verify the most recent request
      expect(jasmine.Ajax.requests.mostRecent().url).toBe(
        "http://apiapptrainingservice.azurewebsites.net/api/Products"
      );
      // At this point the ajax request won't
      // have returned, so any assertions about
      // intermediate states (like spinners)
      // can be run here.
      expect(completedFunction).not.toHaveBeenCalled();

      // tell the request what response should look like
      jasmine.Ajax.requests.mostRecent().response({
        status: 200,
        contentType: "application/json",
        responseText: JSON.stringify(responseObject)
      });
      // Now that we've told the request
      // to respond, our callback gets called.
      expect(completedFunction).toHaveBeenCalledWith(
        JSON.stringify(responseObject)
      );
    });
  });
});
